    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String sql = "select * from Admin where Id=? and Password=? and Nama= ?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtID.getText());
            stat.setString(3, txtNAME.getText());
            stat.setString(2, txtPASSWORD.getText().toLowerCase());
            rs = stat.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Welcome Admin", "Log In", JOptionPane.INFORMATION_MESSAGE);
                Sistem_Penjualan_Pulsa w = new Sistem_Penjualan_Pulsa();
                w.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Wrong Password or ID", "Access Denied", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_loginActionPerformed

    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String sql = "insert into Admin (Id, Password, Nama) values (?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtID.getText());
            stat.setString(3, txtNAME.getText());
            stat.setString(2, txtPASSWORD.getText().toLowerCase());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Saved", "Register", JOptionPane.INFORMATION_MESSAGE);
            kosong();
            txtID.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Can't Saved" + e, "Register", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_registerActionPerformed

