    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            String sql = "update Admin set Password=?, Nama=? where Id='" + txtID.getText() + "'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(2, txtNAME.getText());
            stat.setString(1, txtPASSWORD.getText().toLowerCase());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Saved", "Update", JOptionPane.INFORMATION_MESSAGE);
            kosong();
            txtID.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Can't Save" + e, "Update", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Delete?", "Delete", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from Admin where Id = '" + txtID.getText() + "'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Succes");
                kosong();
                txtID.requestFocus();
                datatable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error" + e);
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

